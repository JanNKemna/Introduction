# calculations with shortcuts

num1 = float(input("Please give me your first number: "))
num2 = float(input("Please give me a second number: "))

res1 = num1
res1 += num2
print("addition: "+str(res1))
res1 = num1
res1 -= num2
print("substraction: "+str(res1))
res1 = num1
res1 /= num2
print("division: "+str(res1))
res1 = num1
res1 *= num2
print("multiplication: "+str(res1))

